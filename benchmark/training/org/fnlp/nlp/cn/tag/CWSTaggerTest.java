package org.fnlp.nlp.cn.tag;


import java.util.ArrayList;
import org.fnlp.ml.types.Dictionary;
import org.junit.Assert;
import org.junit.Test;


public class CWSTaggerTest {
    static CWSTagger tag;

    @Test
    public void testTagString() {
        String str = "??????????????";
        String s = CWSTaggerTest.tag.tag(str);
        Assert.assertTrue(s.equals("??? ??? ??? ?? ???"));
        ArrayList<String> al = new ArrayList<String>();
        al.add("???");
        al.add("???");
        al.add("?????");
        Dictionary dict = new Dictionary(false);
        dict.addSegDict(al);
        CWSTaggerTest.tag.setDictionary(dict);
        s = CWSTaggerTest.tag.tag(str);
        Assert.assertTrue(s.equals("??? ??? ??? ?????"));
    }

    @Test
    public void testTagString2() {
        String str = "?????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????????";
        String s = CWSTaggerTest.tag.tag(str);
        System.out.println(s);
        Assert.assertTrue(s.startsWith("?? ?? "));
    }

    @Test
    public void testTagString1() {
        String str = "????? ?????????????????? ???????????? ?????????????????? ?????????????????????? ???????? ???? ?????";
        String s = CWSTaggerTest.tag.tag(str);
        System.out.println(s);
        // assertTrue(s.equals("??? ??? ??? ?? ???"));
        ArrayList<String> al = new ArrayList<String>();
        al.add("????");
        Dictionary dict = new Dictionary(false);
        dict.addSegDict(al);
        CWSTaggerTest.tag.setDictionary(dict);
        s = CWSTaggerTest.tag.tag(str);
        System.out.println(s);
        Assert.assertTrue(((s.indexOf("????")) != (-1)));
    }
}

