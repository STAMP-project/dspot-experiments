package com.taobao.weex.ui.component;


import Constants.Event.CLICK;
import Constants.Name.COLOR;
import Constants.Name.DISABLED;
import Constants.Name.FONT_FAMILY;
import Constants.Name.FONT_SIZE;
import Constants.Name.FONT_STYLE;
import Constants.Name.FONT_WEIGHT;
import Constants.Name.LINES;
import Constants.Name.LINE_HEIGHT;
import Constants.Name.OPACITY;
import Constants.Name.TEXT_ALIGN;
import Constants.Name.TEXT_DECORATION;
import Constants.Name.TEXT_OVERFLOW;
import com.taobao.weappplus_sdk.BuildConfig;
import com.taobao.weex.dom.WXDomObject;
import com.taobao.weex.dom.WXTextDomObject;
import java.util.HashMap;
import java.util.Map;
import org.junit.Assert;
import org.junit.Test;
import org.junit.runner.RunWith;
import org.robolectric.RobolectricTestRunner;
import org.robolectric.annotation.Config;


/**
 * Created by gulin on 16/2/4.
 */
@RunWith(RobolectricTestRunner.class)
@Config(constants = BuildConfig.class)
public class WXTextTest {
    private WXText mWXText;

    private WXDiv mParent;

    private WXTextDomObject mDomObject;

    private WXDomObject mParentDomObj;

    @Test
    public void testSetProperties() throws Exception {
        mWXText.setProperty(LINES, null);
        mWXText.setProperty(FONT_SIZE, null);
        mWXText.setProperty(FONT_WEIGHT, null);
        mWXText.setProperty(FONT_STYLE, null);
        mWXText.setProperty(COLOR, null);
        mWXText.setProperty(TEXT_DECORATION, null);
        mWXText.setProperty(FONT_FAMILY, null);
        mWXText.setProperty(TEXT_ALIGN, null);
        mWXText.setProperty(TEXT_OVERFLOW, null);
        mWXText.setProperty(LINE_HEIGHT, null);
    }

    @Test
    public void testCreateView() {
        mWXText.createView();
        Assert.assertNotNull(mWXText.getHostView());
    }

    @Test
    public void testSetLayout() {
        testCreateView();
        mWXText.setLayout(mDomObject);
        Assert.assertNotNull(mWXText.getHostView().getLayoutParams());
        Assert.assertEquals(100, mWXText.getHostView().getLayoutParams().height);
        Assert.assertEquals(100, mWXText.getHostView().getLayoutParams().width);
    }

    @Test
    public void testSetPadding() {
        testCreateView();
        mWXText.setPadding(mDomObject.getPadding(), mDomObject.getBorder());
        Assert.assertEquals(0, mWXText.getHostView().getPaddingLeft());
        Assert.assertEquals(0, mWXText.getHostView().getPaddingTop());
        Assert.assertEquals(0, mWXText.getHostView().getPaddingRight());
        Assert.assertEquals(0, mWXText.getHostView().getPaddingBottom());
    }

    @Test
    public void testBind() {
        testCreateView();
        // mWXText.bind(null);
        mWXText.applyLayoutAndEvent(mWXText);
        mWXText.bindData(mWXText);
        Assert.assertNotNull(mWXText.getHostView().getLayoutParams());
        Assert.assertEquals(100, mWXText.getHostView().getLayoutParams().height);
        Assert.assertEquals(100, mWXText.getHostView().getLayoutParams().width);
        Assert.assertEquals(0, mWXText.getHostView().getPaddingLeft());
        Assert.assertEquals(0, mWXText.getHostView().getPaddingTop());
        Assert.assertEquals(0, mWXText.getHostView().getPaddingRight());
        Assert.assertEquals(0, mWXText.getHostView().getPaddingBottom());
    }

    @Test
    public void testAddEvent() {
        testBind();
        mWXText.addEvent(CLICK);
        Assert.assertTrue(mWXText.getHostView().isEnabled());
        mWXText.getHostView().performClick();
    }

    @Test
    public void testUpdateProperties() {
        testBind();
        Map<String, Object> prop = new HashMap<>();
        prop.put(DISABLED, "false");
        prop.put(OPACITY, 0.8F);
        mWXText.updateProperties(prop);
        Assert.assertTrue(mWXText.getHostView().isEnabled());
        Assert.assertTrue((((mWXText.getHostView().getAlpha()) >= 0.799) && ((mWXText.getHostView().getAlpha()) <= 0.811)));
        prop.put(DISABLED, "true");
        mWXText.updateProperties(prop);
        Assert.assertFalse(mWXText.getHostView().isEnabled());
    }
}

