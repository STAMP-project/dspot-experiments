package org.opentripplanner.routing.graph;


import org.junit.Assert;
import org.junit.Test;
import org.opentripplanner.routing.alertpatch.AlertPatch;
import org.opentripplanner.util.ArrayUtils;


public class GraphTest {
    @Test
    public final void testAlertPatch() {
        final AlertPatch[][] alertPatches = new AlertPatch[10][];
        Graph graph = new Graph();
        Vertex vertex0 = new SimpleConcreteVertex(graph, "Vertex 0", 0, 0);
        Vertex vertex1 = new SimpleConcreteVertex(graph, "Vertex 1", 0, 180);
        Edge edge0 = new SimpleConcreteEdge(vertex0, vertex1);
        Edge edge1 = new SimpleConcreteEdge(vertex1, vertex0);
        AlertPatch alertPatch0 = new AlertPatch();
        AlertPatch alertPatch1 = new AlertPatch();
        AlertPatch alertPatch2 = new AlertPatch();
        AlertPatch alertPatch3 = new AlertPatch();
        alertPatch0.setId("A");
        alertPatch1.setId("A");
        alertPatch2.setId("B");
        alertPatch3.setId("C");
        graph.addAlertPatch(null, null);
        graph.addAlertPatch(null, alertPatch0);
        graph.addAlertPatch(null, alertPatch3);
        graph.addAlertPatch(edge0, null);
        graph.removeAlertPatch(edge1, null);
        graph.removeAlertPatch(null, alertPatch1);
        graph.removeAlertPatch(null, alertPatch2);
        graph.removeAlertPatch(null, null);
        alertPatches[0] = graph.getAlertPatches(null);
        alertPatches[1] = graph.getAlertPatches(edge0);
        graph.addAlertPatch(edge0, alertPatch0);
        alertPatches[2] = graph.getAlertPatches(edge0);
        graph.addAlertPatch(edge0, alertPatch1);
        alertPatches[3] = graph.getAlertPatches(edge1);
        graph.addAlertPatch(edge1, alertPatch3);
        alertPatches[4] = graph.getAlertPatches(edge0);
        graph.removeAlertPatch(edge0, alertPatch1);
        alertPatches[5] = graph.getAlertPatches(edge0);
        graph.addAlertPatch(edge1, alertPatch2);
        alertPatches[6] = graph.getAlertPatches(edge1);
        graph.removeAlertPatch(edge1, alertPatch2);
        graph.removeAlertPatch(edge1, alertPatch2);
        alertPatches[7] = graph.getAlertPatches(edge1);
        graph.addAlertPatch(edge0, alertPatch0);
        graph.addAlertPatch(edge0, alertPatch1);
        graph.addAlertPatch(edge0, alertPatch2);
        graph.addAlertPatch(edge0, alertPatch3);
        alertPatches[8] = graph.getAlertPatches(edge0);
        graph.removeAlertPatch(edge0, alertPatch2);
        graph.removeAlertPatch(edge0, alertPatch1);
        graph.removeAlertPatch(edge0, alertPatch3);
        alertPatches[9] = graph.getAlertPatches(edge0);
        Assert.assertEquals(0, alertPatches[0].length);
        Assert.assertFalse(ArrayUtils.contains(alertPatches[0], alertPatch0));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[0], alertPatch1));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[0], alertPatch2));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[0], alertPatch3));
        Assert.assertEquals(0, alertPatches[1].length);
        Assert.assertFalse(ArrayUtils.contains(alertPatches[1], alertPatch0));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[1], alertPatch1));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[1], alertPatch2));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[1], alertPatch3));
        Assert.assertEquals(1, alertPatches[2].length);
        Assert.assertTrue(ArrayUtils.contains(alertPatches[2], alertPatch0));
        Assert.assertTrue(ArrayUtils.contains(alertPatches[2], alertPatch1));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[2], alertPatch2));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[2], alertPatch3));
        Assert.assertEquals(0, alertPatches[3].length);
        Assert.assertFalse(ArrayUtils.contains(alertPatches[3], alertPatch0));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[3], alertPatch1));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[3], alertPatch2));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[3], alertPatch3));
        Assert.assertEquals(1, alertPatches[4].length);
        Assert.assertTrue(ArrayUtils.contains(alertPatches[4], alertPatch0));
        Assert.assertTrue(ArrayUtils.contains(alertPatches[4], alertPatch1));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[4], alertPatch2));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[4], alertPatch3));
        Assert.assertEquals(0, alertPatches[5].length);
        Assert.assertFalse(ArrayUtils.contains(alertPatches[5], alertPatch0));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[5], alertPatch1));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[5], alertPatch2));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[5], alertPatch3));
        Assert.assertEquals(2, alertPatches[6].length);
        Assert.assertFalse(ArrayUtils.contains(alertPatches[6], alertPatch0));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[6], alertPatch1));
        Assert.assertTrue(ArrayUtils.contains(alertPatches[6], alertPatch2));
        Assert.assertTrue(ArrayUtils.contains(alertPatches[6], alertPatch3));
        Assert.assertEquals(1, alertPatches[7].length);
        Assert.assertFalse(ArrayUtils.contains(alertPatches[7], alertPatch0));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[7], alertPatch1));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[7], alertPatch2));
        Assert.assertTrue(ArrayUtils.contains(alertPatches[7], alertPatch3));
        Assert.assertEquals(3, alertPatches[8].length);
        Assert.assertTrue(ArrayUtils.contains(alertPatches[8], alertPatch0));
        Assert.assertTrue(ArrayUtils.contains(alertPatches[8], alertPatch1));
        Assert.assertTrue(ArrayUtils.contains(alertPatches[8], alertPatch2));
        Assert.assertTrue(ArrayUtils.contains(alertPatches[8], alertPatch3));
        Assert.assertEquals(0, alertPatches[9].length);
        Assert.assertFalse(ArrayUtils.contains(alertPatches[9], alertPatch0));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[9], alertPatch1));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[9], alertPatch2));
        Assert.assertFalse(ArrayUtils.contains(alertPatches[9], alertPatch3));
    }
}

