package org.hibernate.userguide.mapping.generated;


import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.util.Date;
import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.Id;
import org.hibernate.annotations.ValueGenerationType;
import org.hibernate.jpa.test.BaseEntityManagerFunctionalTestCase;
import org.hibernate.tuple.AnnotationValueGeneration;
import org.hibernate.tuple.GenerationTiming;
import org.hibernate.tuple.ValueGenerator;
import org.junit.Test;


/**
 *
 *
 * @author Vlad Mihalcea
 */
// end::mapping-in-memory-generated-value-example[]
public class InMemoryValueGenerationTest extends BaseEntityManagerFunctionalTestCase {
    @Test
    public void test() {
        doInJPA(this::entityManagerFactory, ( entityManager) -> {
            org.hibernate.userguide.mapping.generated.Event dateEvent = new org.hibernate.userguide.mapping.generated.Event();
            entityManager.persist(dateEvent);
        });
    }

    // tag::mapping-in-memory-generated-value-example[]
    // tag::mapping-in-memory-generated-value-example[]
    @Entity(name = "Event")
    public static class Event {
        @Id
        @GeneratedValue
        private Long id;

        @Column(name = "`timestamp`")
        @InMemoryValueGenerationTest.FunctionCreationTimestamp
        private Date timestamp;

        // Constructors, getters, and setters are omitted for brevity
        // end::mapping-in-memory-generated-value-example[]
        public Event() {
        }

        public Long getId() {
            return id;
        }

        public Date getTimestamp() {
            return timestamp;
        }
    }

    // end::mapping-in-memory-generated-value-example[]
    // tag::mapping-in-memory-generated-value-example[]
    @ValueGenerationType(generatedBy = InMemoryValueGenerationTest.FunctionCreationValueGeneration.class)
    @Retention(RetentionPolicy.RUNTIME)
    public @interface FunctionCreationTimestamp {}

    public static class FunctionCreationValueGeneration implements AnnotationValueGeneration<InMemoryValueGenerationTest.FunctionCreationTimestamp> {
        @Override
        public void initialize(InMemoryValueGenerationTest.FunctionCreationTimestamp annotation, Class<?> propertyType) {
        }

        /**
         * Generate value on INSERT
         *
         * @return when to generate the value
         */
        public GenerationTiming getGenerationTiming() {
            return GenerationTiming.INSERT;
        }

        /**
         * Returns the in-memory generated value
         *
         * @return {@code true}
         */
        public ValueGenerator<?> getValueGenerator() {
            return ( session, owner) -> new Date();
        }

        /**
         * Returns false because the value is generated by the database.
         *
         * @return false
         */
        public boolean referenceColumnInSql() {
            return false;
        }

        /**
         * Returns null because the value is generated in-memory.
         *
         * @return null
         */
        public String getDatabaseGeneratedReferencedColumnValue() {
            return null;
        }
    }
}

